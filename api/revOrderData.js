import client from '../utils/client';
// API CALLS FOR ORDER DATA
// PROMISES GO ON THIS PAGE

const endpoint = client.databaseURL;

// GET/READ ORDERS
const getOrderTotals = (uid) => new Promise((resolve, reject) => {
  fetch(`${endpoint}/orders.json?orderBy="uid"&equalTo="${uid}"`, {
    method: 'GET',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then((data) => {
      if (data) {
        resolve(Object.values(data));
      } else {
        resolve([]);
      }
    })
    .catch(reject);
});

export default getOrderTotals;
